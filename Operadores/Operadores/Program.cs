int a = 60;
Console.WriteLine(a);

a += 18;
Console.WriteLine(a);

int b = a -= 12;
Console.WriteLine(b);

int c = b *= 6;
Console.WriteLine(c);

int d = c /= 12;
Console.WriteLine(d);

int e = d %= 6;
Console.WriteLine(e);

int f = ++e;
Console.WriteLine(f);
int g = --f;
Console.WriteLine(g);

